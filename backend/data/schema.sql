CREATE DATABASE vinh_khanh_narration_db 




—
-- =========================================================
-- DATABASE SCHEMA
-- Project: Multilingual Automatic Narration for Vinh Khanh Food Street
-- DBMS: PostgreSQL
-- Database: vinh_khanh_narration_db
