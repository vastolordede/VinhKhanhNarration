        ON UPDATE CASCADE
        ON DELETE SET NULL,

    CONSTRAINT chk_feedbacks_rating
        CHECK (rating BETWEEN 1 AND 5),

    CONSTRAINT chk_feedbacks_target
        CHECK (
            (
                place_id IS NOT NULL
                AND dish_id IS NULL
                AND narration_id IS NULL
            )
            OR
            (
                dish_id IS NOT NULL
                AND place_id IS NULL
                AND narration_id IS NULL
            )
            OR
            (
                narration_id IS NOT NULL
                AND place_id IS NULL
                AND dish_id IS NULL
            )
        )
);


