-- =========================================================
-- 4. MIGRATE places.place_type + places.trigger_mode
-- =========================================================

ALTER TABLE places
ADD COLUMN place_type_id BIGINT,
ADD COLUMN trigger_mode_id BIGINT;

UPDATE places p
SET place_type_id = pt.place_type_id
FROM place_types pt
WHERE p.place_type = pt.code;

UPDATE places p
SET trigger_mode_id = tm.trigger_mode_id
FROM trigger_modes tm
WHERE p.trigger_mode = tm.code;

UPDATE places
SET place_type_id = (
    SELECT place_type_id FROM place_types WHERE code = 'Other'
)
WHERE place_type_id IS NULL;

UPDATE places
SET trigger_mode_id = (
    SELECT trigger_mode_id FROM trigger_modes WHERE code = 'Enter'
)
WHERE trigger_mode_id IS NULL;

ALTER TABLE places
ALTER COLUMN place_type_id SET NOT NULL,
ALTER COLUMN trigger_mode_id SET NOT NULL;

ALTER TABLE places
ADD CONSTRAINT fk_places_place_type
FOREIGN KEY (place_type_id)
REFERENCES place_types(place_type_id)
ON UPDATE CASCADE
ON DELETE RESTRICT;

ALTER TABLE places
ADD CONSTRAINT fk_places_trigger_mode
FOREIGN KEY (trigger_mode_id)
REFERENCES trigger_modes(trigger_mode_id)
ON UPDATE CASCADE
ON DELETE RESTRICT;

ALTER TABLE places
DROP COLUMN place_type,
DROP COLUMN trigger_mode;


