-- 14. UPDATED_AT TRIGGER
-- Tự động cập nhật updated_at khi update dòng dữ liệu
-- =========================================================

CREATE OR REPLACE FUNCTION set_updated_at()
RETURNS TRIGGER AS $$
BEGIN
    NEW.updated_at = CURRENT_TIMESTAMP;
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;


CREATE TRIGGER trg_admin_users_updated_at
