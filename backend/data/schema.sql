-- =========================================================
-- GUEST POI STATES
-- Lưu trạng thái khách đang ở trong / ngoài vùng POI
-- =========================================================

CREATE TABLE guest_poi_states (
    guest_session_id VARCHAR(100) NOT NULL,
    place_id BIGINT NOT NULL,

    is_inside BOOLEAN NOT NULL DEFAULT FALSE,

    last_entered_at TIMESTAMP,
    last_exited_at TIMESTAMP,
    last_triggered_at TIMESTAMP,
    cooldown_until TIMESTAMP,

    last_distance_meters NUMERIC(10, 2),

    updated_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,

    PRIMARY KEY (guest_session_id, place_id),

    CONSTRAINT fk_guest_poi_states_session
        FOREIGN KEY (guest_session_id)
        REFERENCES guest_sessions(guest_session_id)
        ON UPDATE CASCADE
        ON DELETE CASCADE,

    CONSTRAINT fk_guest_poi_states_place
        FOREIGN KEY (place_id)
        REFERENCES places(place_id)
        ON UPDATE CASCADE
        ON DELETE CASCADE
);


-- =========================================================
-- GEOFENCE EVENTS
-- Log sự kiện geofence: Enter / Near / Exit
-- =========================================================

CREATE TABLE geofence_events (
    event_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,

    guest_session_id VARCHAR(100) NOT NULL,

    place_id BIGINT NOT NULL,
    narration_id BIGINT,

    event_type VARCHAR(20) NOT NULL,
    event_status VARCHAR(30) NOT NULL DEFAULT 'Detected',

    user_latitude NUMERIC(10, 7),
    user_longitude NUMERIC(10, 7),

    distance_meters NUMERIC(10, 2),

    detected_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    processed_at TIMESTAMP,

    note TEXT,

    CONSTRAINT fk_geofence_events_session
        FOREIGN KEY (guest_session_id)
        REFERENCES guest_sessions(guest_session_id)
        ON UPDATE CASCADE
        ON DELETE CASCADE,

    CONSTRAINT fk_geofence_events_place
        FOREIGN KEY (place_id)
        REFERENCES places(place_id)
        ON UPDATE CASCADE
        ON DELETE CASCADE,

    CONSTRAINT fk_geofence_events_narration
        FOREIGN KEY (narration_id)
        REFERENCES narration_contents(narration_id)
        ON UPDATE CASCADE
        ON DELETE SET NULL,

    CONSTRAINT chk_geofence_events_event_type
        CHECK (event_type IN ('Enter', 'Near', 'Exit')),

    CONSTRAINT chk_geofence_events_status
        CHECK (event_status IN ('Detected', 'IgnoredDebounce', 'IgnoredCooldown', 'Queued', 'Played'))
);


