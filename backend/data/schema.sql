-- =========================================================
-- 1. ADMIN USERS
-- Tài khoản nội bộ: Admin, ContentManager, Translator, Reviewer
-- Role fix cứng bằng CHECK constraint
-- =========================================================

CREATE TABLE admin_users (
    admin_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,

    full_name VARCHAR(100) NOT NULL,
    email VARCHAR(150) NOT NULL UNIQUE,
    password_hash VARCHAR(255) NOT NULL,

    role VARCHAR(50) NOT NULL,

    is_active BOOLEAN NOT NULL DEFAULT TRUE,

    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,

    CONSTRAINT chk_admin_users_role
        CHECK (role IN ('Admin', 'ContentManager', 'Translator', 'Reviewer'))
);


-- =========================================================
-- 2. LANGUAGES
-- Danh sách ngôn ngữ hệ thống hỗ trợ
-- =========================================================

CREATE TABLE languages (
    language_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,

    language_code VARCHAR(10) NOT NULL UNIQUE,
    language_name VARCHAR(100) NOT NULL,

    is_default BOOLEAN NOT NULL DEFAULT FALSE,
    is_active BOOLEAN NOT NULL DEFAULT TRUE,

    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP
);


-- =========================================================
-- 3. PLACES
-- Địa điểm / quán ăn / khu vực ở phố ẩm thực Vĩnh Khánh
-- =========================================================

CREATE TABLE places (
    place_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,

    place_name VARCHAR(150) NOT NULL,
    place_type VARCHAR(50) NOT NULL,

    address VARCHAR(255),
    description TEXT,

    latitude NUMERIC(10, 7),
    longitude NUMERIC(10, 7),

    opening_hours VARCHAR(100),
    image_url VARCHAR(500),

    is_active BOOLEAN NOT NULL DEFAULT TRUE,

    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,

    CONSTRAINT chk_places_place_type
        CHECK (place_type IN ('Restaurant', 'FoodStall', 'Area', 'CheckInPoint', 'Other'))
);


-- =========================================================
-- 4. DISH CATEGORIES
-- Danh mục món ăn
-- =========================================================

CREATE TABLE dish_categories (
    category_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,

    category_name VARCHAR(100) NOT NULL UNIQUE,
    description TEXT,

    is_active BOOLEAN NOT NULL DEFAULT TRUE,

    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP
);


-- =========================================================
-- 5. DISHES
-- Món ăn
-- =========================================================

CREATE TABLE dishes (
    dish_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,

    dish_name VARCHAR(150) NOT NULL,

    category_id BIGINT NOT NULL,

    description TEXT,
    image_url VARCHAR(500),

    average_price NUMERIC(12, 2),
    is_signature_dish BOOLEAN NOT NULL DEFAULT FALSE,

    is_active BOOLEAN NOT NULL DEFAULT TRUE,

    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,

    CONSTRAINT fk_dishes_category
        FOREIGN KEY (category_id)
        REFERENCES dish_categories(category_id)
        ON UPDATE CASCADE
        ON DELETE RESTRICT,

    CONSTRAINT chk_dishes_average_price
        CHECK (average_price IS NULL OR average_price >= 0)
);


-- =========================================================
-- 6. PLACE DISHES
-- Bảng trung gian: quán nào bán món nào
-- Quan hệ nhiều - nhiều giữa places và dishes
-- =========================================================

CREATE TABLE place_dishes (
    place_dish_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,

    place_id BIGINT NOT NULL,
    dish_id BIGINT NOT NULL,

    price NUMERIC(12, 2),
    is_recommended BOOLEAN NOT NULL DEFAULT FALSE,
    note TEXT,

    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,

    CONSTRAINT fk_place_dishes_place
        FOREIGN KEY (place_id)
        REFERENCES places(place_id)
        ON UPDATE CASCADE
        ON DELETE CASCADE,

    CONSTRAINT fk_place_dishes_dish
        FOREIGN KEY (dish_id)
        REFERENCES dishes(dish_id)
        ON UPDATE CASCADE
        ON DELETE CASCADE,

    CONSTRAINT uq_place_dishes_place_dish
        UNIQUE (place_id, dish_id),

    CONSTRAINT chk_place_dishes_price
        CHECK (price IS NULL OR price >= 0)
);


-- =========================================================
-- 7. NARRATION CONTENTS
-- Nội dung thuyết minh gốc
-- Có thể thuộc Place, Dish hoặc General
-- =========================================================

CREATE TABLE narration_contents (
    narration_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,

    title VARCHAR(200) NOT NULL,
    original_text TEXT NOT NULL,

    content_type VARCHAR(50) NOT NULL,

    place_id BIGINT,
    dish_id BIGINT,

    created_by BIGINT NOT NULL,

    is_active BOOLEAN NOT NULL DEFAULT TRUE,

    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,

    CONSTRAINT fk_narration_contents_place
        FOREIGN KEY (place_id)
        REFERENCES places(place_id)
        ON UPDATE CASCADE
        ON DELETE SET NULL,

    CONSTRAINT fk_narration_contents_dish
        FOREIGN KEY (dish_id)
        REFERENCES dishes(dish_id)
        ON UPDATE CASCADE
        ON DELETE SET NULL,

    CONSTRAINT fk_narration_contents_created_by
        FOREIGN KEY (created_by)
        REFERENCES admin_users(admin_id)
        ON UPDATE CASCADE
        ON DELETE RESTRICT,

    CONSTRAINT chk_narration_contents_content_type
        CHECK (content_type IN ('Place', 'Dish', 'General')),

    CONSTRAINT chk_narration_contents_target
        CHECK (
            (
                content_type = 'Place'
                AND place_id IS NOT NULL
                AND dish_id IS NULL
            )
            OR
            (
                content_type = 'Dish'
                AND dish_id IS NOT NULL
                AND place_id IS NULL
            )
            OR
            (
                content_type = 'General'
                AND place_id IS NULL
                AND dish_id IS NULL
            )
        )
);


-- =========================================================
-- 8. NARRATION TRANSLATIONS
-- Bản dịch của nội dung thuyết minh theo từng ngôn ngữ
-- =========================================================

CREATE TABLE narration_translations (
    translation_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,

    narration_id BIGINT NOT NULL,
    language_id BIGINT NOT NULL,

    translated_title VARCHAR(200) NOT NULL,
    translated_text TEXT NOT NULL,

    translation_source VARCHAR(50) NOT NULL DEFAULT 'Manual',

    reviewed_by BIGINT,
    is_reviewed BOOLEAN NOT NULL DEFAULT FALSE,

    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,

    CONSTRAINT fk_narration_translations_narration
        FOREIGN KEY (narration_id)
        REFERENCES narration_contents(narration_id)
        ON UPDATE CASCADE
        ON DELETE CASCADE,

    CONSTRAINT fk_narration_translations_language
        FOREIGN KEY (language_id)
        REFERENCES languages(language_id)
        ON UPDATE CASCADE
        ON DELETE RESTRICT,

    CONSTRAINT fk_narration_translations_reviewed_by
        FOREIGN KEY (reviewed_by)
        REFERENCES admin_users(admin_id)
        ON UPDATE CASCADE
        ON DELETE SET NULL,

    CONSTRAINT uq_narration_translations_narration_language
        UNIQUE (narration_id, language_id),

    CONSTRAINT chk_narration_translations_source
        CHECK (translation_source IN ('Manual', 'AI', 'GoogleTranslate'))
);


-- =========================================================
-- 9. AUDIO FILES
-- File âm thanh cho từng bản dịch
-- =========================================================

CREATE TABLE audio_files (
    audio_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,

    translation_id BIGINT NOT NULL,

    audio_url VARCHAR(500) NOT NULL,

    voice_name VARCHAR(100),
    voice_gender VARCHAR(20),

    duration_seconds INTEGER,
    file_format VARCHAR(20) NOT NULL DEFAULT 'mp3',

    generated_by VARCHAR(50) NOT NULL DEFAULT 'TTS',

    is_active BOOLEAN NOT NULL DEFAULT TRUE,

    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,

    CONSTRAINT fk_audio_files_translation
        FOREIGN KEY (translation_id)
        REFERENCES narration_translations(translation_id)
        ON UPDATE CASCADE
        ON DELETE CASCADE,

    CONSTRAINT chk_audio_files_voice_gender
        CHECK (
            voice_gender IS NULL
            OR voice_gender IN ('Male', 'Female', 'Neutral')
        ),

    CONSTRAINT chk_audio_files_duration
        CHECK (duration_seconds IS NULL OR duration_seconds >= 0),

    CONSTRAINT chk_audio_files_format
        CHECK (file_format IN ('mp3', 'wav', 'ogg', 'm4a')),

    CONSTRAINT chk_audio_files_generated_by
        CHECK (generated_by IN ('TTS', 'ManualUpload'))
);


-- =========================================================
-- 10. QR CODES
-- QR có thể trỏ đến Place, Dish hoặc Narration
-- Trong 1 dòng chỉ được có 1 target chính
-- =========================================================

CREATE TABLE qr_codes (
    qr_code_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,

    qr_code_value VARCHAR(255) NOT NULL UNIQUE,
    qr_code_image_url VARCHAR(500),

    target_type VARCHAR(50) NOT NULL,

    place_id BIGINT,
    dish_id BIGINT,
    narration_id BIGINT,

    is_active BOOLEAN NOT NULL DEFAULT TRUE,

    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,

    CONSTRAINT fk_qr_codes_place
        FOREIGN KEY (place_id)
        REFERENCES places(place_id)
        ON UPDATE CASCADE
        ON DELETE SET NULL,

    CONSTRAINT fk_qr_codes_dish
        FOREIGN KEY (dish_id)
        REFERENCES dishes(dish_id)
        ON UPDATE CASCADE
        ON DELETE SET NULL,

    CONSTRAINT fk_qr_codes_narration
        FOREIGN KEY (narration_id)
        REFERENCES narration_contents(narration_id)
        ON UPDATE CASCADE
        ON DELETE SET NULL,

    CONSTRAINT chk_qr_codes_target_type
        CHECK (target_type IN ('Place', 'Dish', 'Narration')),

    CONSTRAINT chk_qr_codes_target
        CHECK (
            (
                target_type = 'Place'
                AND place_id IS NOT NULL
                AND dish_id IS NULL
                AND narration_id IS NULL
            )
            OR
            (
                target_type = 'Dish'
                AND dish_id IS NOT NULL
                AND place_id IS NULL
                AND narration_id IS NULL
            )
            OR
            (
                target_type = 'Narration'
                AND narration_id IS NOT NULL
                AND place_id IS NULL
                AND dish_id IS NULL
            )
        )
);


-- =========================================================
-- 11. LISTENING HISTORIES
-- Lịch sử nghe của khách anonymous
-- Không có user_id
-- Dùng guest_session_id để phân biệt phiên khách
